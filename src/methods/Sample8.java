package methods;

public class Sample8 {
    // instance var
    private String name;
    private int age;

    // mutator/setter methods to store data
    public void setName(String name)
    {
        this.name=name;
    }
    public void setAge(int age)
    {
        this.age=age;
    }

    // accessor/getter methods to read data
    public String getName()
    {
        return name;
    }
    public int getAge()
    {
        return age;
    }
}
