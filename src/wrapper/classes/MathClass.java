package wrapper.classes;

import java.math.*;

// all the methods of math class are static and returns double value

public class MathClass {
    public static void main(String[] args)
    {
        System.out.println(Math.sin(90));
        System.out.println(Math.cos(90));
        System.out.println(Math.tan(180));
        System.out.println(Math.log(0.5));
        System.out.println(Math.log10(0.5));
        System.out.println(Math.pow(5,3));
        System.out.println(Math.sqrt(4));
        System.out.println(Math.abs(-4));
        System.out.println(Math.ceil(4.5));
        System.out.println(Math.floor(4.5));
        System.out.println(Math.min(2,4));
        System.out.println(Math.max(5,9));
        System.out.println(Math.round(4.6));
        System.out.println(Math.round(4.4));
        System.out.println(Math.random());
        System.out.println(Math.toRadians(90));
        System.out.println(Math.toDegrees(90));
    }
}
